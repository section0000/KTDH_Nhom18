    public void drawCircleMidPoint(double xO, double yO, double r) {
        xO = xO * step + width / 2;
        yO = height / 2 - yO * step;
        r = r * step;

        double x = 0;
        double y = r;
        double p = 1 - r;

        put8Pixel(xO, yO, x, y);

        while (x < y) {
            if (p < 0) {
                p += 2 * x + 3;
            } else {
                p += 2 * (x - y) + 5;
                y--;
            }
            x++;
            put8Pixel(xO, yO, x, y);
        }
    }

    public void drawEllipseMidPoint(double xO, double yO, double a, double b) {
        xO = xO * step + width / 2;
        yO = height / 2 - yO * step;
        a = a * step;
        b = b * step;

        double x, y, fx, fy, a2, b2, p, q;
        x = 0;
        y = b;
        a2 = a * a;
        b2 = b * b;
        fx = 0;
        fy = 2 * a2 * y;
        put4Pixel(xO, yO, x, y);
        p = Math.round(b2 - a2 * b + a2 / 4);

        int count = 0;
        while (fx < fy) {
            count++;
            x++;
            fx = fx + 2 * b2;
            if (p < 0) {
                p = p + b2 * (2 * x + 3);
            } else {
                y--;
                p = p + b2 * (2 * x + 3) + a2 * (2 - 2 * y);
                fy = fy - 2 * a2;
            }
            if (count >= 11 && count <= 15) {
                if (count == 15) {
                    count = 0;
                }
                continue;
            }
            put4Pixel(xO, yO, x, y);
        }

        int count1 = 0;
        q = Math.round(b2 * (x + 0.5) * (x + 0.5) + a2 * (y - 1) * (y - 1) - a2 * b2);
        while (y > 0) {
            count1++;
            y--;
            fy = fy - 2 * a2;
            if (q < 0) {
                x++;
                q = q + b2 * (2 * x + 2) + a2 * (3 - 2 * y);
                fx = fx + 2 * b2;
            } else {
                q = q + a2 * (3 - 2 * y);
            }
            if (count1 >= 11 && count1 <= 15) {
                if (count1 == 15) {
                    count1 = 0;
                }
                continue;
            }
            put4Pixel(xO, yO, x, y);
        }
    }

